//Operadores Aritméticos
/* soma: + 
* subtração: -
* divisão: /
* multiplicação: *
* resto da divisão: % 
* incremento: ++
* decremento: --
*/ 

fun main( ){
    var num1 = 20
    var num2 = 10

    println(num1 + num2)
    println(num1 - num2)
    println(num1 / num2)
    println(num1 * num2)
    println(num1 % num2)
    //o ++ ou -- vem ANTES da variável ou número
    println(++num1)
    println(--num2)
}