//Operadores Relacionais

/* Igual: ==
* Menor: <
* Maior: >
* Menor igual: <=
* Maior igual: >=
* Diferente: != 
*/

fun main( ){
    var valor1 = 10
    var valor2 = 20

    println(valor1 == valor2)
    //resposta false

    println(valor1 > valor2)
    //false

    println(valor1 < valor2)
    //true

    println(valor1 != valor2)
    //true
}